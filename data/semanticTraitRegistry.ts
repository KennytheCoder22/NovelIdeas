// NovelIdeas semantic trait registry
//
// Purpose:
// - Keep semantic trait vocabulary convergent across swipe decks.
// - Prevent drift, typos, and near-duplicate traits in semantic blocks.
// - Provide a lightweight normalization layer for:
//     contentTraits
//     toneTraits
//     characterTraits
//     storyTraits
//     aversionTraits
//
// Recommended placement:
// - Put this file next to `data/tagNormalizationMap.ts`
// - Import it anywhere semantic blocks are validated, normalized, or linted.
//
// Design principles:
// - Reuse before inventing
// - Specific but reusable
// - Fail-closed for unknown categories
// - Unknown traits are preserved by default unless explicitly deprecated,
//   because deck authoring may still be evolving. Consumers can choose to
//   warn on unknown traits via `findUnknownTraits`.

