function slugifyTrait(input: string): string {
  return String(input)
    .trim()
    .toLowerCase()
    .replace(/[^\w\s-]/g, "")
    .replace(/[\s-]+/g, "_");
}

export function getApprovedTraits(category: SemanticCategory): readonly SemanticTrait[] {
  return semanticTraitRegistry[category];
}

export function isApprovedSemanticTrait(
  category: SemanticCategory,
  trait: string | null | undefined
): boolean {
  if (!trait) return false;
  return semanticTraitRegistry[category].includes(trait as SemanticTrait);
}

export function normalizeSemanticTrait(
  trait: string | null | undefined,
  category?: SemanticCategory
): SemanticTrait | null {
  if (!trait) return null;

  const cleaned = slugifyTrait(trait);

  const viaDeprecated = deprecatedSemanticTraits[cleaned];
  const viaAlias = semanticTraitAliases[cleaned];
  const candidate = viaDeprecated ?? viaAlias ?? cleaned;

  if (category) {
    return isApprovedSemanticTrait(category, candidate) ? candidate : candidate;
  }

  return candidate;
}

export function normalizeSemanticTraits(
  traits: Array<string | null | undefined>,
  category: SemanticCategory
): SemanticTrait[] {
  const seen = new Set<string>();
  const out: SemanticTrait[] = [];

  for (const raw of traits) {
    const normalized = normalizeSemanticTrait(raw, category);
    if (!normalized) continue;
    if (seen.has(normalized)) continue;
    seen.add(normalized);
    out.push(normalized);
  }

  return out;
}

export function normalizeSemanticBlock(block: Partial<Record<SemanticCategory, string[]>>) {
  return {
    contentTraits: normalizeSemanticTraits(block.contentTraits ?? [], "contentTraits"),
    toneTraits: normalizeSemanticTraits(block.toneTraits ?? [], "toneTraits"),
    characterTraits: normalizeSemanticTraits(block.characterTraits ?? [], "characterTraits"),
    storyTraits: normalizeSemanticTraits(block.storyTraits ?? [], "storyTraits"),
    aversionTraits: normalizeSemanticTraits(block.aversionTraits ?? [], "aversionTraits"),
  };
}

export function findUnknownTraits(block: Partial<Record<SemanticCategory, string[]>>) {
  const unknown: Array<{ category: SemanticCategory; trait: string }> = [];

  (Object.keys(semanticTraitRegistry) as SemanticCategory[]).forEach((category) => {
    for (const raw of block[category] ?? []) {
      const normalized = normalizeSemanticTrait(raw, category);
      if (!normalized) continue;
      if (!isApprovedSemanticTrait(category, normalized)) {
        unknown.push({ category, trait: normalized });
      }
    }
  });

  return unknown;
}
